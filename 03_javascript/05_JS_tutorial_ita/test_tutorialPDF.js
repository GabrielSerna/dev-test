// ex. 05 trasformare una stringa in un array
function ex05() {
  console.log(`
  ========================================================
  ex: 05 trasformare una stringa in un array
  `);

  let str1 = 'abcdefg';
  let arr1 = [...str1];                                                         // ['a', 'b', 'c', 'd', 'e', 'f', 'g']
  console.log(arr1);

  let str2 = 'qwerty'
  let arr2 = arr1.concat(...str2);                                              // ['a', 'b', 'c', 'd', 'e', 'f', 'g']
  console.log(arr2);

  let str3 = 'riel'
  let arr3 = ['g', 'a', 'b',...[...str3]];                                      // ['g', 'a', 'b', 'r', 'i', 'e', 'l']
  console.log(arr3);

  let arr4 = ['s', 'e', 'r',...['n', 'a']];                                     // ['s', 'e', 'r', 'n', 'a']

  console.log(arr4);
}

