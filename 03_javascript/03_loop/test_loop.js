// while ... do

function ex3() {
  console.log(`
  ========================================================
  ex 3: while do`
  );

  let a3 = [1, 2, 3, 4, 5];
  console.log(`a1: ${a3}`);
  let counter = 0;

  while (counter < a3.length) {
      console.log(a3[counter]*10);
      counter++;
  }
}

