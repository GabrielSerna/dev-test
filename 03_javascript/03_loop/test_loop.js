// ex. for
function ex1() {
  console.log(`
  ========================================================
  ex 1 for`
  );

  let a1 = [1, 2, 3, 4, 5];
  console.log(`a1: ${a1}`);

  for ( i = 0; i < a1.length; i++ ) {
      console.log(a1[i]*10);
  }
}

//==============================================================================
// forEach

function ex2() {
  console.log(`
  ========================================================
  ex 2 forEach`
  );

  let a2 = [1, 2, 3, 4, 5];
  console.log(`a2: ${a2}`);

  a2.forEach(element => console.log(element*10));
}

